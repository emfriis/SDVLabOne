console.log(numSum); // Prints value of numSum.

var numSub = numFirst - numSecond; // Assigns value to numSub equal to first number minus the second number.
console.log(numSub); // Prints value of numSub.

var numMult = numFirst * numSecond; // Assigns value to numMult equal to first number multiplied by the second number.
console.log(numMult); // Prints value of numMult.

var numDiv = numFirst / numSecond; // Assigns value to numDiv equal to first number divided the second number.
console.log(numDiv); // Prints value of numDiv.

var numRem = numFirst / numSecond; // Assigns value to numRem equal to the remainder of the first number divided by the second number.
console.log(numRem); // Prints value of numRem.

